package suanfa.face;

public class StrBean {
	private String name;
	private int hz;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getHz() {
		return hz;
	}
	public void setHz(int hz) {
		this.hz = hz;
	}
	public StrBean(String name, int hz) {
		super();
		this.name = name;
		this.hz = hz;
	}
	
	
}
